package stylus.calendars;

/**
 * Ways to present an era, weekday, or month name.
 */
public enum Form {
    /** The tiniest form, typically one character. */
    TINY,

    /** The tiniest form that's unambiguous */
    UNAMBIGUOUS,

    /** The short form, typically three characters */
    SHORT,

    /** The full form. */
    FULL
}
